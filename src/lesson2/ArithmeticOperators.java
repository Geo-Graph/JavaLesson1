package lesson2;

public class ArithmeticOperators {

    public static void main(String[] args) {

        int x = 20, y = -1;
        int result = x + y;
        System.out.println(result);

        int fer = 35, far = 2;
        double res = (double) fer / far;
        System.out.println(res);
    }
}
